print("pls enter a number")
num1 = input()
print("pls enter a second number")
num2 = input()
print("pls enter your operation sign")
sign = input()

if sign == "+":
    res = int(num1) + int(num2)
    print("the answer is "+str(res))

elif sign == "-":
    res = int(num1) - int(num2)
    print("the answer is "+str(res))

elif sign == "*":
    res = int(num1) * int(num2)
    print("the answer is "+str(res))

elif sign == "/":
    res = int(num1) + int(num2)
    print("the answer is "+str(res))

